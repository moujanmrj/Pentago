import java.util.*;
/**
 * this class is for the marbles in the board
 * it holds their color
 *
 * @author Moujan Mirjalili
 * @version 2020
 */
public class Marble
{
    //this field shows the color of the pentago marbles
    private String color;

    /**
     * this method returns the color of the marbles
     *
     * @return color of the pentago marbles
     */
    public String getColor() { return color; }

    /**
     * this method sets the color of the marbles
     *
     * @param color color of the pentago marbles
     */
    public void setColor(String color) { this.color = color; }
}
